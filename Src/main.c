/* StartPIDTask function */
void StartPIDTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
    int32_t error = encoder-setPoint;
    if( fabs(error) > tolerance)
    {
      read = arm_pid_f32(&myPID,(float32_t)encoder-setPoint);
      //printf("PID: %f\n",foo);
      
      if(read > 0)
      {
        if(read > maxPWM)
          read = maxPWM;
        __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, (int32_t)read);
        __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 0);
      }
      else
      {
        read = fabs(read);
        if(read > maxPWM)
          read = maxPWM;
        __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 0);
        __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, (int32_t)read);
      }
    }
    else
    {
      __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 0);
      __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 0);
      //arm_pid_reset_f32(&myPID);
    }
    osDelay(20);
  }
  /* USER CODE END 5 */ 
}

/* StartReportTask function */
void StartReportTask(void const * argument)
{
  /* USER CODE BEGIN StartReportTask */
  /* Infinite loop */
  for(;;)
  {
    sample = __HAL_TIM_GET_COUNTER(&htim1);
    
    encoder += (sample - sampleOld);
    sampleOld = sample; 
    
    if(j %100 == 0)
    {
      printf("%i\t%i\t%f\n",j,encoder,read);
    }
    j++;
    osDelay(10);
  }
  /* USER CODE END StartReportTask */
}

